# Criando uma string com caracteres não imprimíveis
string_com_espacos = "Hello\tworld!\nWelcome to Python."

# Usando o método ascii() para obter a representação ASCII
representacao_ascii = ascii(string_com_espacos)

# Imprimindo a representação ASCII
print(representacao_ascii)


# 'Hello\tworld!\nWelcome to Python.'
