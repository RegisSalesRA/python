"""
A função divmod() em Python é usada para realizar a divisão de dois números e retornar o resultado como uma tupla contendo o resultado da divisão e o resto da divisão.
 Aqui está um exemplo de como usar divmod():
"""

# Usando divmod() para realizar a divisão
resultado = divmod(20, 6)

# Imprimindo o resultado
print("Resultado da divisão:", resultado)
