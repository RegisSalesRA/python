"""
A função len() em Python é usada para obter o número de elementos em um objeto iterável, como uma lista, tupla, string, dicionário, conjunto, etc.
 Ela retorna um número inteiro que representa o comprimento (número de elementos) do objeto. Aqui estão alguns exemplos de como usar len():
"""


minha_lista = [1, 2, 3, 4, 5]
comprimento_lista = len(minha_lista)
print("Comprimento da lista:", comprimento_lista)  # Saída: 5


minha_string = "Python"
comprimento_string = len(minha_string)
print("Comprimento da string:", comprimento_string)  # Saída: 6


meu_dicionario = {"a": 1, "b": 2, "c": 3}
comprimento_dicionario = len(meu_dicionario)
print("Comprimento do dicionário:", comprimento_dicionario)  # Saída: 3


minha_tupla = (10, 20, 30, 40, 50)
comprimento_tupla = len(minha_tupla)
print("Comprimento da tupla:", comprimento_tupla)  # Saída: 5
