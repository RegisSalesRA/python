"""

A função chr() em Python é usada para retornar uma representação de caractere (string) correspondente a um valor inteiro que representa
 o código Unicode de um caractere. Aqui está um exemplo de como usar chr():
"""

# Usando chr() para obter o caractere correspondente ao código Unicode
codigo_unicode = 65  # Código Unicode para 'A'
caractere = chr(codigo_unicode)

print(f"O caractere correspondente a {codigo_unicode} é: {caractere}")


