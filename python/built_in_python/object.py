"""
A função object() em Python é uma função interna que cria uma nova instância de objeto.
 Em Python, todos os objetos herdam de uma classe base chamada object.
   No entanto, você geralmente não precisa usar a função object() diretamente, 
   pois a criação de instâncias de object é feita implicitamente ao criar qualquer objeto em Python.

Aqui está um exemplo simples de como a função object() pode ser usada para criar uma instância de objeto:
"""

# Criando uma instância de objeto usando object()
objeto = object()

# Exibindo a representação do objeto
print(objeto)  # Saída: <object object at 0x...>
