"""

A função round() em Python é usada para arredondar um número para um número inteiro ou para uma quantidade específica de casas decimais.
 Ela aceita dois argumentos: o número que você deseja arredondar e, opcionalmente, o número de casas decimais para arredondar (o padrão é 0 casas decimais).

Aqui estão alguns exemplos de como usar round():
"""

numero = 3.7
arredondado = round(numero)
print(arredondado)  # Saída: 4


numero_decimal = 5.6789
arredondado_2_casas = round(numero_decimal, 2)
print(arredondado_2_casas)  # Saída: 5.68


numero_negativo = -4.3
arredondado = round(numero_negativo)
print(arredondado)  # Saída: -4


