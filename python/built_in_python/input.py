"""
A função input() em Python é usada para receber entrada de dados do usuário por meio do teclado. 
Ela permite que você exiba uma mensagem ao usuário, aguarde que o usuário digite algum texto e, em seguida,
 retorne o texto fornecido como uma string. Aqui está um exemplo de como usar input():
"""

# Solicitando ao usuário que digite seu nome
nome = input("Digite seu nome: ")

# Exibindo uma mensagem de saudação com o nome fornecido
print(f"Olá, {nome}!")
