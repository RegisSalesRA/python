"""
A função abs() em Python é usada para calcular o valor absoluto de um número, ou seja, 
o valor positivo do número, independentemente de ser positivo ou negativo.
 O valor absoluto de um número é sempre não negativo. Aqui está um exemplo explicado:
"""

numero = -5
absoluto = abs(numero)
print("Numero original:", numero)
print("Valor absoluto:", absoluto)


